                <h1 class="text-white margin-top-bottom-40 text-size-60 text-line-height-1">Profile</h1>
                <p>Kepolisian Resor Pati (Polres Pati) merupakan pelaksana tugas Kepolisian Negara
                        Republik Indonesia di wilayah Kabupaten Pati. Polres Pati yang berada di bawah
                        jajaran Polda Jawa Tengah memiliki tugas utama dalam hal memelihara keamanan dan
                        ketertiban, menegakkan hukum, memberikan perlindungan, pengayoman dan pelayanan
                        kepada Masyarakat di wilayah hukum yang menjadi tanggung jawabnya.<br>
                        <br>Secara total wilayah hukum Polres Pati memiliki total luas 1.503,68 km2 dan terdiri dari 21
                        kecamatan 401 Desa serta dengan Jumalah Penduduk 1.517.669 Jiwa. Dalam kesehariannya Polres Pati
                        dipimpin oleh seorang perwira menengah Polri yang berpangkat Ajun Komisaris Besar Polisi dan
                        diwakili oleh seorang Wakil Kepala Polres yang berpangkat Komisaris Polisi.<br>
                        <br>Beberapa jenis satuan Fungsi yang berada dibawah naungan Polres Pati antara lain Satuan Reserse Kriminal, Satuan Lalu Lintas, Satuan Reserse Narkoba, Satuan Intelkam, Satuan Binmas, Sie Humas, Sie Propam, Sie Pengawasan dan SPKT, Sedangkan beberapa jenis layanan kepolisian yang tersedia seperti misalnya layanan Pengaduan Masyarakat (Laporan Polisi dan Laporan Kehilangan), SIM, SKCK, SP2HP, ijin keramaian, pengawalan dan pengaduan komplain Masyarakat terhadap pelayanan Polri.
                </p>
